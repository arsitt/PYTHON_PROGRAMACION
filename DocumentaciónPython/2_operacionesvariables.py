#Se pueden hacer diferentes operaciones con las variables.

a = 10
b = 6
c = 2
d = 3

# Suma de las variabls
print(a+b)
# Resta de las variables
print(a-b)
#Multiplicación de las variables
print(a*b)
#Division de las variables, esta división nos da decimales
print(a/b)
#Para recibir solo los cocientes(cuantas veces hay que dividir el numero para dar con el resultado o acercarse)
print(a//b)
#Para recibir solo el resto 
print(a%b)
#Para las potencias de los números
print(d**c)

